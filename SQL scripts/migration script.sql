);

CREATE TABLE Answers(
	[Id] [int] NOT NULL PRIMARY KEY IDENTITY,
	[QuestionId] [int] NOT NULL FOREIGN KEY REFERENCES Questions(Id) ON DELETE CASCADE,
	[Text] [nvarchar](max) NOT NULL,
	[IsCorrect] [bit] NOT NULL
);

