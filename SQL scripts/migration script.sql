GO

CREATE TABLE Questions(
	[Id] [int] NOT NULL PRIMARY KEY IDENTITY,
	[TestId] [int] NOT NULL FOREIGN KEY REFERENCES Tests(Id) ON DELETE CASCADE,
	[Text] [nvarchar](max) NOT NULL,
